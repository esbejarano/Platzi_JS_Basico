var numero = 100;

switch (numero) {
    case 1:
        console.log('Soy un uno!');
        break;
    case 10: 
        console.log('Soy un diez!');
        break;
    case 100: 
        console.log('Soy un cien!');
        break;
    default:
        console.log('No esta en los casos')
        break;
}
